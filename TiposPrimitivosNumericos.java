package fundamentos;

public class TiposPrimitivosNumericos {
	
	public static void main(String[] args) {
		
		double a = 1; //conversão implicita
		System.out.println(a);
		
		float b = (float)1.0f; //conversao explicita (cast)
		
		int c = 469;
		byte d = (byte)c; //conversao explicita (cast)
		System.out.println(d);
		
		double e = 1;
		int f = (int) e; //conversao explicita (cast)
		System.out.println();
		
		
				
		
		
	}

}
